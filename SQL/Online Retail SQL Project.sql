-- =====================================================
-- 10. Identification of customers driving the majority of business revenue
-- =====================================================
-- Columns used: CustomerID, Quantity, UnitPrice
-- Query:
WITH CustomerRevenue AS (
    SELECT
        CustomerID,
        SUM(Quantity * UnitPrice) AS revenue
    FROM dbo.online_retail_clean
    WHERE CustomerID IS NOT NULL
      AND Quantity > 0
      AND UnitPrice > 0
    GROUP BY CustomerID
),
RevenueContribution AS (
    SELECT
        CustomerID,
        revenue,
        SUM(revenue) OVER (ORDER BY revenue DESC) AS cumulative_revenue,
        SUM(revenue) OVER () AS total_revenue
    FROM CustomerRevenue
)
SELECT
    CustomerID,
    ROUND(revenue, 2) AS revenue,
    ROUND(cumulative_revenue * 100.0 / total_revenue, 2) AS cumulative_revenue_percentage
FROM RevenueContribution
WHERE cumulative_revenue <= total_revenue * 0.80
ORDER BY revenue DESC;
