    CustomerID,
    ROUND(revenue, 2) AS revenue,
    ROUND(running_revenue * 100.0 / total_revenue, 2) AS cumulative_revenue_percentage
FROM RevenueContribution
WHERE running_revenue <= total_revenue * 0.80
