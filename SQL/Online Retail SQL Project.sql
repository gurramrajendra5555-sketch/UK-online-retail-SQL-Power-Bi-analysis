-- =====================================================
-- 9. Revenue contribution by customer segment
-- =====================================================
-- Columns used: CustomerID, Quantity, UnitPrice
-- Query:
WITH CustomerRevenue AS (
    SELECT
        CustomerID,
        SUM(Quantity * UnitPrice) AS monetary
    FROM dbo.online_retail_clean
    WHERE CustomerID IS NOT NULL
      AND Quantity > 0
      AND UnitPrice > 0
    GROUP BY CustomerID
),
Segments AS (
    SELECT
        CustomerID,
        monetary,
        CASE
            WHEN monetary >= 10000 THEN 'High Value'
            WHEN monetary >= 3000 THEN 'Medium Value'
            ELSE 'Low Value'
        END AS customer_segment
    FROM CustomerRevenue
)
SELECT
    customer_segment,
    COUNT(*) AS customer_count,
    ROUND(SUM(monetary), 2) AS segment_revenue,
    ROUND(SUM(monetary) * 100.0 / SUM(SUM(monetary)) OVER (), 2) AS revenue_percentage
FROM Segments
GROUP BY customer_segment
ORDER BY segment_revenue DESC;
