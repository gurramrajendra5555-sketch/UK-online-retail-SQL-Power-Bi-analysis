      AND Quantity > 0
      AND UnitPrice > 0
    GROUP BY CustomerID
),
RevenueContribution AS (
    SELECT
        CustomerID,
        revenue,
        SUM(revenue) OVER (ORDER BY revenue DESC) AS running_revenue,
        SUM(revenue) OVER () AS total_revenue
    FROM CustomerRevenue
)
