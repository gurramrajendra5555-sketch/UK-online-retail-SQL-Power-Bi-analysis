-- 8. Fastest-growing revenue month
-- =====================================================
-- Columns used: InvoiceDate, Quantity, UnitPrice
-- Query:
WITH MonthlyRevenue AS (
    SELECT
        YEAR(InvoiceDate) AS sales_year,
        MONTH(InvoiceDate) AS sales_month,
        SUM(Quantity * UnitPrice) AS revenue
    FROM dbo.online_retail_clean
    WHERE Quantity > 0
      AND UnitPrice > 0
    GROUP BY YEAR(InvoiceDate), MONTH(InvoiceDate)
),
Growth AS (
    SELECT
        sales_year,
        sales_month,
        revenue,
        LAG(revenue) OVER (ORDER BY sales_year, sales_month) AS previous_month_revenue
    FROM MonthlyRevenue
)
SELECT
    sales_year,
    sales_month,
    ROUND(revenue, 2) AS revenue,
    ROUND(previous_month_revenue, 2) AS previous_month_revenue,
    ROUND(
        (revenue - previous_month_revenue) * 100.0 /
        NULLIF(previous_month_revenue, 0),
        2
    ) AS growth_percentage
FROM Growth
WHERE previous_month_revenue IS NOT NULL
ORDER BY growth_percentage DESC;
